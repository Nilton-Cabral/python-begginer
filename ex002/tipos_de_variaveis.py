# declarando diferentes tipos de variaveis

# Variaveis do tipo string
qualquer = "Palavra"

# Imprimindo o tipo de variavel
print(type(qualquer))

# Variavel Numerica
numero = 16

# Imprimindo o tipo de variavel
print(type(numero))

# Variaveis do tipo float
numero_float = 12.5

# Imprimindo o tipo de variavel
print(type(numero_float))

# variaveis boleanas
boolean1, boolean2 = True, False

# Imprimindo o tipo de variavel
print(type(boolean1))

# Imprimindo o tipo de variavel
print(type(boolean2))

