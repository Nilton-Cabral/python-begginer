# Trabalhando com operadores em python

# Definindo constantes
NUMERO1 = 10
NUMERO2 = 10

# Adicao
# adicionado os dois numeros
soma = NUMERO1 + NUMERO2

# Imprimindo o resultado
print(soma)

# -------------------------------------
# Subtração
subtracao = NUMERO1 - NUMERO2

# Imprimindo o resultado
print(subtracao)

# -------------------------------------
# multiplicação
print(NUMERO1 * NUMERO2)

# -------------------------------------
# divisão
print(NUMERO1/NUMERO2)

# -------------------------------------
# Modulo ou resto da divisão
print(NUMERO1%NUMERO2)

# -------------------------------------
# Exponenciação
print(NUMERO1**NUMERO2)

# -------------------------------------
# divisão inteira
print(NUMERO1//NUMERO2)