# Estruturas de decisão ou condicionais

a = int(input("Digite um numero: "))

# definido o numero de acao
numero_acao = 7

# construindo a estrutura de decisão
if a > numero_acao:
    print("Desculpe o seu numero é maior que o sorteado")
if a < numero_acao:
    print("Desculpe o seu numero é menor que o sorteado")
if a == numero_acao:
    print("Você acertou!")